/*
Kullanıcıya 20'den başlayarak
 her adımda 7 artarak 100'e kadar
  yazdırmak için bir Java programı yazın.
   (Eğer 100 yazdırmanız gerekiyorsa, yazdırın, ancak 100'ü aşmayın.)
 */ 
public class Solution3 {
    public static void main(String[] args) {
        for(int i = 20;i<100;i+=7){
            System.out.print(i+" ");
        }
    }
}
